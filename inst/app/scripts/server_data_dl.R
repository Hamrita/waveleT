output$dltabRaw1 <- downloadHandler(
  filename=function(){
    variable=fy1()
    name=paste0("Tab_Raw_",variable,".csv")
    return(name)
  },
  content=function(file){
    write.table(xy1y2()[,c(1,2)],
                file,
                sep=";",
                row.names=FALSE)}
)
