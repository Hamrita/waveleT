output$dlfigRaw1 <- downloadHandler(
  filename=function(){
    variable=fy1()
    name=paste0("Fig_Raw_",variable,".",input$graph_format)
    return(name)
  },
  content=function(file){
    fgraph=get(input$graph_format)
    fgraph(file,width=input$width,height=input$height)
    f_plotRaw1()
    dev.off()
  }
)
